package model;

/**
 * the class player
 */
public class Player {
		
		/**
		 * the x
		 */
		int x=0;
		
		/**
		 * the y
		 */
		int y=1;
		
		/**
		 * get x
		 */
		public int getX() {
			return x;
		}
		
		/**
		 * set x
		 */
		public void setX(int x) {
			this.x = x;
		}
		
		/**
		 * get y
		 */
		public int getY() {
			return y;
		}
		
		/**
		 * set y
		 */
		public void setY(int y) {
			this.y = y;
		}
		
		
		
}
